var womensData = JSON.parse(localStorage.getItem("womensData"));
var cartData = JSON.parse(localStorage.getItem("cartData")) || [];

womensData.map(function (elem) {
    var div = document.createElement("div");
    div.setAttribute("id", "main-div")

    var image = document.createElement("img");
    image.setAttribute("id", "product-image")
    image.setAttribute("src", elem.image_url);

    var name = document.createElement("h4");
    name.setAttribute("id", "name-h1");
    name.textContent = elem.name;

    var p_div = document.createElement("div");
    p_div.setAttribute("id", "p_main");

    var p1 = document.createElement("p");
    p1.textContent = elem.price;

    var p2 = document.createElement("p");
    p2.textContent = elem.strikedoffprice;
    p2.style.textDecoration = "line-through";

    var btn = document.createElement("button");
    btn.setAttribute("id", "add-button");
    btn.textContent = "Add to Cart";
    btn.addEventListener("click", function () {
        addToCart(elem, btn);
    });

    p_div.append(p1, p2);

    div.append(image, name, p_div, btn);

    document.querySelector("#container").append(div);
});

function addToCart(elem, btn) {
    cartData.push(elem);
    localStorage.setItem("cartData", JSON.stringify(cartData));
    btn.style.backgroundColor = "red";
    btn.textContent = "Item Added";
}

document.querySelector("#cart-btn").addEventListener("click", function () {
    window.location.href = "cart.html";
});




